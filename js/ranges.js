export function cellAction(row, col, scenario, position) {
  const hand = handAt(row, col);
  const chart = scenario === "RFI" ? RFI[position] : VS_UTG[position];
  if (!chart) return { r: 0, c: 0 };
  const v = chart[hand];
  if (!v) return { r: 0, c: 0 };
  return { r: v[0], c: v[1] };
}
